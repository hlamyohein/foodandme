<h2>Help Center</h2>

<div class="faq-item">
    <h4>How do I place an order?</h4>
    <p>Simply browse our list of restaurants, add items to your cart from your chosen restaurant, and proceed to checkout. At checkout, you'll confirm your delivery address and payment method.</p>
</div>

<div class="faq-item">
    <h4>What payment methods are accepted?</h4>
    <p>We accept Cash on Delivery (COD), KBZPay, and WavePay. You can select your preferred method during the checkout process.</p>
</div>

<div class="faq-item">
    <h4>How is the delivery fee calculated?</h4>
    <p>The delivery fee is calculated based on the distance from the restaurant to your delivery address. You can see the final fee on the checkout page after setting your location.</p>
</div>

<div class="faq-item">
    <h4>How can I track my order?</h4>
    <p>Currently, real-time order tracking is not available. You will receive an estimated delivery time once your order is confirmed.</p>
</div>

<div class="faq-item">
    <h4>Who can I contact for support?</h4>
    <p>If you have any issues with your order, please contact our support team at <strong>support@foodandme.com</strong> or call us at <strong>+95 9 123 456 789</strong>.</p>
</div>